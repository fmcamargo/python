'''
Crie um programa que leia um número e mostre seu dobro, triplo e raiz quadrada
'''

n = int(input('Digite um número: '))

dobro = n * 2
triplo = n * 3
rq = n ** 0.5

print(f'O número digitado é {n}\nO dobro deste valor é {dobro}\nO triplo desde valor é {triplo}\nA raiz quadrada deste valor é {rq:.3f}')